namespace ExerciciosCsharp.Logica
{
    public static class Exercicio1
    {
        public static string ParOuImpar(int numero)
        {
            if (numero % 2 == 0)
            {
                return "É UM NÚMERO PAR";
            }
            else
            {
                return "É UM NÚMERO IMPAR";
            }
        }
    }
}

